"""
This module contains the calculator functions for basic arithmetic operations.
"""

def add(first_number, second_number):
    """
    Add two numbers and return the result.

    Parameters:
    first_number (int or float): The first number.
    second_number (int or float): The second number.

    Returns:
    int or float: The sum of the two numbers.
    """
    return first_number + second_number

def subtract(first_number, second_number):
    """
    Subtract second_number from first_number and return the result.

    Parameters:
    first_number (int or float): The number from which to subtract.
    second_number (int or float): The number to subtract.

    Returns:
    int or float: The result of the subtraction.
    """
    return first_number - second_number
